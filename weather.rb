weather = 'snowy'
case weather
when 'sunny'
	puts "Watch out for the heat; wear short clothing!"
when 'cloudy'
	puts "The weather's perfect. Anything will do."
when 'windy'
	puts "If it's a cold wind, put on a jacket!"
when 'rainy'
	puts "Make sure to bring an umbrella!"
when 'snowy'
	puts "Bundle up and enjoy the snow!"
else 
	puts "Stay inside."
end